class Product {
  final int id;
  final String label;
  final String countryName;
  final String title;
  final String desc;
  final int noofTours;
  final double rating;
  final double price;
  final String imgUrl;
  final String desc_detail;

  Product({
    required this.id, 
    required this.label,
    required this.countryName,
    required this.title,
    required this.desc,
    required this.noofTours,
    required this.rating,
    required this.price,
    required this.imgUrl,
    required this.desc_detail,
  });
}
